const { contextBridge, ipcRenderer } = require('electron')
contextBridge.exposeInMainWorld('api', {
  getSettings: () => ipcRenderer.invoke('get-settings'),
  saveSettings: (d) => ipcRenderer.invoke('save-settings', d),
  getDashboard: () => ipcRenderer.invoke('get-dashboard'),
  getCustomers: (s) => ipcRenderer.invoke('get-customers', s),
  getCustomer: (id) => ipcRenderer.invoke('get-customer', id),
  addCustomer: (d) => ipcRenderer.invoke('add-customer', d),
  updateCustomer: (id, d) => ipcRenderer.invoke('update-customer', { id, data: d }),
  updateCustomerBalance: (id, balance) => ipcRenderer.invoke('update-customer-balance', { id, balance }),
  deleteCustomer: (id) => ipcRenderer.invoke('delete-customer', id),
  getInvoices: (f) => ipcRenderer.invoke('get-invoices', f),
  getNextInvoiceNumber: () => ipcRenderer.invoke('get-next-invoice-number'),
  addInvoice: (d) => ipcRenderer.invoke('add-invoice', d),
  updateInvoice: (id, d) => ipcRenderer.invoke('update-invoice', { id, data: d }),
  deleteInvoice: (id) => ipcRenderer.invoke('delete-invoice', id),
  recordPayment: (d) => ipcRenderer.invoke('record-payment', d),
  getBills: (f) => ipcRenderer.invoke('get-bills', f),
  getNextBillNumber: () => ipcRenderer.invoke('get-next-bill-number'),
  addBill: (d) => ipcRenderer.invoke('add-bill', d),
  updateBill: (id, d) => ipcRenderer.invoke('update-bill', { id, data: d }),
  deleteBill: (id) => ipcRenderer.invoke('delete-bill', id),
  recordBillPayment: (d) => ipcRenderer.invoke('record-bill-payment', d),
  getExpenses: (f) => ipcRenderer.invoke('get-expenses', f),
  addExpense: (d) => ipcRenderer.invoke('add-expense', d),
  updateExpense: (id, d) => ipcRenderer.invoke('update-expense', { id, data: d }),
  deleteExpense: (id) => ipcRenderer.invoke('delete-expense', id),
  getPDCReceivable: () => ipcRenderer.invoke('get-pdc-receivable'),
  addPDCReceivable: (d) => ipcRenderer.invoke('add-pdc-receivable', d),
  updatePDCReceivable: (id, s) => ipcRenderer.invoke('update-pdc-receivable', { id, status: s }),
  deletePDC: (id) => ipcRenderer.invoke('delete-pdc', id),
  getReport: (p) => ipcRenderer.invoke('get-report', p),
  getNotifications: () => ipcRenderer.invoke('get-notifications'),
  markNotificationRead: (id) => ipcRenderer.invoke('mark-notification-read', id),
  backupDatabase: () => ipcRenderer.invoke('backup-database'),
  gdriveGetStatus: () => ipcRenderer.invoke('gdrive-get-status'),
  gdriveConnect: (c) => ipcRenderer.invoke('gdrive-connect', c),
  gdriveSetCode: (d) => ipcRenderer.invoke('gdrive-set-code', d),
  gdriveBackup: () => ipcRenderer.invoke('gdrive-backup'),
  gdriveDisconnect: () => ipcRenderer.invoke('gdrive-disconnect'),
})
